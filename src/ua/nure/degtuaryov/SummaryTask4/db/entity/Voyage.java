package ua.nure.degtuaryov.SummaryTask4.db.entity;

import java.sql.Timestamp;

/**
 * 
 * Voyage class entity from database, extends Entity
 * 
 * @author Degtuaryow
 *
 */
public class Voyage extends Entity {

	private static final long serialVersionUID = 8086016371082249763L;

	private long routeId;

	private long trainId;

	private Timestamp arrivalTime;

	private Timestamp departureTime;

	private int coupeSeat;

	private int reservedSeat;

	private int generalSeat;

	public long getRouteId() {
		return routeId;
	}

	public void setRouteId(long routeId) {
		this.routeId = routeId;
	}

	public long getTrainId() {
		return trainId;
	}

	public void setTrainId(long trainId2) {
		this.trainId = trainId2;
	}

	public Timestamp getArrivalTime() {
		return arrivalTime;
	}

	public void setArrivalTime(Timestamp arrivalTime) {
		this.arrivalTime = arrivalTime;
	}

	public Timestamp getDepartureTime() {
		return departureTime;
	}

	public void setDepartureTime(Timestamp departureTime) {
		this.departureTime = departureTime;
	}

	public int getCoupeSeat() {
		return coupeSeat;
	}

	public void setCoupeSeat(int coupeSeat) {
		this.coupeSeat = coupeSeat;
	}

	public int getReservedSeat() {
		return reservedSeat;
	}

	public void setReservedSeat(int reservedSeat) {
		this.reservedSeat = reservedSeat;
	}

	public int getGeneralSeat() {
		return generalSeat;
	}

	public void setGeneralSeat(int generalSeat) {
		this.generalSeat = generalSeat;
	}

	@Override
	public String toString() {
		return "Voyage [routeId=" + routeId + ", trainId=" + trainId + ", arrivalTime=" + arrivalTime
				+ ", departureTime=" + departureTime + ", coupe_seat=" + coupeSeat + ", reserved_seat=" + reservedSeat
				+ ", general_seat=" + generalSeat + "]";
	}	
}
