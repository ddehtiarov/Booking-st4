package ua.nure.degtuaryov.SummaryTask4.db.entity;

import java.sql.Timestamp;

/**
 * 
 * Booking class entity from database
 * 
 * @author Degtuaryow
 *
 */
public class Booking extends Entity {

	private static final long serialVersionUID = 4264922666851210353L;

	private long clientId;

	private long voyageId;

	private long benefitId;

	private long tripId;
	
	private long seatId;

	private int price;

	private Timestamp date;
	
	private Timestamp arrivalTime;
	
	private Timestamp departureTime;
	
	private int seatNumber;
	
	public long getClientId() {
		return clientId;
	}

	public void setClientId(long clientId) {
		this.clientId = clientId;
	}

	public long getVoyageId() {
		return voyageId;
	}

	public void setVoyageId(long voyageId) {
		this.voyageId = voyageId;
	}

	public long getBenefitId() {
		return benefitId;
	}

	public void setBenefitId(long benefitId) {
		this.benefitId = benefitId;
	}
	
	public int getSeatNumber() {
		return seatNumber;
	}

	public void setSeatNumber(int seatNumber) {
		this.seatNumber = seatNumber;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public Timestamp getDate() {
		return date;
	}

	public void setDate(Timestamp date) {
		this.date = date;
	}

	public long getTripId() {
		return tripId;
	}

	public void setTripId(long tripId) {
		this.tripId = tripId;
	}

	public long getSeatId() {
		return seatId;
	}

	public void setSeatId(long seatId) {
		this.seatId = seatId;
	}

	public Timestamp getArrivalTime() {
		return arrivalTime;
	}

	public void setArrivalTime(Timestamp arrivalTime) {
		this.arrivalTime = arrivalTime;
	}

	public Timestamp getDepartureTime() {
		return departureTime;
	}

	public void setDepartureTime(Timestamp departureTime) {
		this.departureTime = departureTime;
	}

	@Override
	public String toString() {
		return "Booking [clientId=" + clientId + ", voyageId=" + voyageId + ", benefitId=" + benefitId + ", tripId="
				+ tripId + ", seatId=" + seatId + ", price=" + price + ", date=" + date + ", arrivalTime=" + arrivalTime
				+ ", departureTime=" + departureTime + ", seatNumber=" + seatNumber + "]";
	}
   
}
